// ── 取得隨機精油（可指定五行/H.O.U.R.篩選）──
window.HL_OILS.random = function(filter){
  var keys = Object.keys(HL_OILS).filter(function(k){ return k.length===3; });
  if(filter){
    if(filter.wx) keys=keys.filter(function(k){return HL_OILS[k].wx===filter.wx;});
    if(filter.hour) keys=keys.filter(function(k){return HL_OILS[k].hour===filter.hour;});
  }
  if(!keys.length) return null;
  var key=keys[Math.floor(Math.random()*keys.length)];
  return Object.assign({id:key},HL_OILS[key]);
};

// ── 根據關鍵詞匹配精油 ──
window.HL_OILS.match = function(keyword){
  var results=[];
  Object.keys(HL_OILS).forEach(function(k){
    if(k.length!==3) return;
    var oil=HL_OILS[k];
    if(oil.tags.indexOf(keyword)>=0) results.push(Object.assign({id:k},oil));
  });
  return results;
};

// ── 取得指定精油 ──
