  function seededShuffle(arr,seed){
    var s=0;for(var i=0;i<seed.length;i++)s=((s<<5)-s)+seed.charCodeAt(i);
    var a=arr.slice();
    for(var i=a.length-1;i>0;i--){s=(s*16807+0)%2147483647;var j=Math.abs(s)%(i+1);var t=a[i];a[i]=a[j];a[j]=t;}
    return a;
  }

