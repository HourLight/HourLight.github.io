module.exports = function handler(req, res) {
  var origin = req.headers.origin || '';
  var allowed = ['https://hourlightkey.com', 'https://www.hourlightkey.com'];
  if (allowed.indexOf(origin) > -1) res.setHeader('Access-Control-Allow-Origin', origin);
  res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') return res.status(200).end();

  var n = parseInt(req.query.n) || 0;

  if (req.query.action === 'list') {
    var spreads = {};
    Object.keys(SPREAD_FRAMEWORKS).forEach(function(k) {
      spreads[k] = SPREAD_FRAMEWORKS[k].title;
    });
    return res.status(200).json({ spreads: spreads });
  }

  var spread = SPREAD_FRAMEWORKS[n];
  if (!spread) {
    return res.status(200).json({
      spread_framework: '',
      general: GENERAL_FRAMEWORK,
      hour: HOUR_FRAMEWORK,
      tone: TONE,
      books: BOOKS
    });
  }

  return res.status(200).json({
    title: spread.title,
    spread_framework: spread.framework,
    general: GENERAL_FRAMEWORK,
    hour: HOUR_FRAMEWORK,
    tone: TONE,
    books: BOOKS
  });
};
