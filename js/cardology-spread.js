// ── 牌面顯示轉換 ──
var SUIT_SYMBOL={'H':'♥','C':'♣','D':'♦','S':'♠'};

function cardDisplay(code){
  if(!code) return '?';
  var suit=code.slice(-1);
  var val=code.slice(0,-1);
  return val+SUIT_SYMBOL[suit];
}

function cardSuitCN(code){
  if(!code) return '';
  return SUIT_NAMES[code.slice(-1)]||'';
}

// ══════ 完整計算函式 ══════
function calcCardologyFull(month,day){
  var key=month+'-'+day;
  var code=BDAY[key];
  if(!code) return null;

  var suit=code.slice(-1);
  var val=code.slice(0,-1);
  var display=cardDisplay(code);
  var pd=PLANETARY_DATA[code];
  var karma=KARMA_CARDS[code]||[];

  // 行星排列牌
  var planetary=[];
  if(pd&&pd.planetary){
    pd.planetary.forEach(function(p){
      planetary.push({
        planet:p.planet,
        ages:p.ages,
        card:cardDisplay(p.card),
        cardCode:p.card,
        isSelf:p.isSelf,
        meaning:CARD_MEANING[p.card]||''
      });
    });
  }

  // 業力牌
  var karmaCards=karma.map(function(k){
    return{card:cardDisplay(k),code:k,meaning:CARD_MEANING[k]||''};
  });

  return{
    // 向下相容（與現有 calcCardology 相同的欄位）
    code:code,
    display:display,
    suitName:SUIT_NAMES[suit]||suit,
    suitElement:SUIT_ELEMENT[suit]||'',
    meaning:CARD_MEANING[code]||'',
    suitColor:(suit==='H'||suit==='D')?'#E53935':'#1a1a2e',

    // 新增欄位
    suitTheme:SUIT_THEME[suit]||'',
    valueTheme:VALUE_THEME[val]||'',
    homePlanet:pd?pd.homePlanet:'',
    planetary:planetary,
    karmaCards:karmaCards,
    isFixed:(karma.length===0)
  };
}

// ══════ 驗證函式（開發用）══════
function verify(){
  var results=[];

  // 1. 確認 Life Spread 有 52 張不重複的牌
  var seen={};
  var dupes=[];
  for(var q=1;q<=52;q++){
    if(seen[LIFE_SPREAD[q]]) dupes.push(LIFE_SPREAD[q]);
    seen[LIFE_SPREAD[q]]=true;
  }
  results.push('Life Spread 重複牌：'+(dupes.length===0?'✓ 無':'✗ '+dupes.join(',')));
  results.push('Life Spread 總數：'+(Object.keys(seen).length===52?'✓ 52張':'✗ '+Object.keys(seen).length+'張'));

  // 2. 驗算逸君 1976/2/5 → JD
  var r1=calcCardologyFull(2,5);
  results.push('逸君(2/5)='+r1.display+' '+(r1.code==='JD'?'✓':'✗ 應為J♦'));

  // 3. 驗算朵朵 1997/11/11 → 9D
  var r2=calcCardologyFull(11,11);
  results.push('朵朵(11/11)='+r2.display+' '+(r2.code==='9D'?'✓':'✗ 應為9♦'));

  // 4. 驗算蘋蘋 2000/1/7 → 9H
  var r3=calcCardologyFull(1,7);
  results.push('蘋蘋(1/7)='+r3.display+' '+(r3.code==='9H'?'✓':'✗ 應為9♥'));

  // 5. 確認行星牌有 7 張
  results.push('逸君行星牌數量：'+(r1.planetary.length===7?'✓ 7張':'✗ '+r1.planetary.length+'張'));

  // 6. 列出逸君的完整行星排列
  results.push('');
  results.push('【逸君 J♦ 完整 Cardology】');
  results.push('本命牌：'+r1.display+' ('+r1.suitName+' / '+r1.suitElement+')');
  results.push('本命行星：'+r1.homePlanet);
  r1.planetary.forEach(function(p){
    results.push('  '+p.planet+' ('+p.ages+')：'+p.card+(p.isSelf?' ← 本命':''));
  });
  results.push('業力牌：'+(r1.karmaCards.length>0?r1.karmaCards.map(function(k){return k.card}).join(', '):'（固定牌，無業力牌）'));

  return results.join('\n');
}

// ── 掛載到全域 ──
window.calcCardologyFull=calcCardologyFull;
