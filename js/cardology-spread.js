// ── 生命牌陣（Life Spread / Spiritual Spread）──
// 數學推導：Richmond 的 Quadration 公式
// Life Spread 位置 q → Natural Spread 位置 p = (27 × q) mod 53
// 即 Natural → Life: p → (2p) mod 53
function buildLifeSpread(){
  // lifeSpread[lifePos] = cardCode
  var ls=new Array(53);
  for(var q=1;q<=52;q++){
    var natPos=(27*q)%53;
    if(natPos===0) natPos=53; // safety
    ls[q]=DECK[natPos];
  }
  return ls;
}

var LIFE_SPREAD=buildLifeSpread();

// ── 從 Life Spread 建構每張牌的行星排列牌 ──
// Life Spread 排成 7 列（Mercury ~ Neptune），同一列的 7 張牌互為行星牌
var PLANETS=['☿水星','♀金星','♂火星','♃木星','♄土星','♅天王','♆海王'];
var PLANET_AGES=['0-12歲','13-25歲','26-38歲','39-51歲','52-64歲','65-77歲','78歲+'];

function buildPlanetaryCards(){
  // 對每張牌，找出它在 Life Spread 的位置，然後找同行的其他牌
  var cardToLifePos={};
  for(var q=1;q<=52;q++){
    cardToLifePos[LIFE_SPREAD[q]]=q;
  }
  
  var result={};
  for(var code in cardToLifePos){
    var pos=cardToLifePos[code];
    var row=Math.floor((pos-1)/7);
    var col=(pos-1)%7;
    var cards=[];
    for(var c=0;c<7;c++){
      var rowPos=row*7+c+1;
      if(rowPos<=52){
        cards.push({
          planet:PLANETS[c],
          ages:PLANET_AGES[c],
          card:LIFE_SPREAD[rowPos],
          isSelf:(c===col)
        });
      }
    }
    result[code]={
      lifePos:pos,
      row:row,
      col:col,
      homePlanet:PLANETS[col],
      planetary:cards
    };
  }
  return result;
}

