// ── 業力牌（Karma Cards）──
// 業力牌 = 在你的 Natural Spread 位置上，Life Spread 放的那張牌
// 即：你站在 Natural 的位置 p，Life Spread 的同一位置 p 放了另一張牌 K
// 你和 K 是業力對
function buildKarmaCards(){
  var result={};
  for(var p=1;p<=52;p++){
    var natCard=DECK[p];         // 你在 Natural 的這個位置
    var lifeCard=LIFE_SPREAD[p]; // Life Spread 同位置的牌
    if(natCard!==lifeCard){
      // 互為業力牌
      if(!result[natCard]) result[natCard]=[];
      result[natCard].push(lifeCard);
    }
    // 如果相同，這張就是「固定牌」（same position in both spreads）
  }
  return result;
}

var KARMA_CARDS=buildKarmaCards();

// ── 找固定牌（Natural 和 Life Spread 位置相同的牌）──
