def escape_json(s: str) -> str:
    return s.replace("\\", "\\\\").replace('"', '\\"').replace("\n", " ")

def build_faq_schema(faqs: list) -> str:
    items = []
    for q, a in faqs:
        items.append(f'{{"@type":"Question","name":"{escape_json(q)}","acceptedAnswer":{{"@type":"Answer","text":"{escape_json(a)}"}}}}')
    return ",".join(items)

def build_faq_html(faqs: list) -> str:
    items = []
    for q, a in faqs:
        items.append(
            f'<div class="faq-item"><div class="faq-q" onclick="this.parentElement.classList.toggle(\'open\')">{q}</div>'
            f'<div class="faq-a"><p>{a}</p></div></div>'
        )
    return "\n".join(items)

